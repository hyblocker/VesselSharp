	public class VesselEngine
	{
		/// <summary>
		/// The window of this engine instance
		/// </summary>
		public VesselWindow Window;

		/// <summary>
		/// The graphics device responsible for driving this instance of the engine
		/// </summary>
		public GraphicsDevice GraphicsDevice;

		/// <summary>
		/// Whether or not Vessel will log events to the logger
		/// </summary>
		public bool EnableLogging
		{
			get
			{
				return (Logger != null && typeof(Logger) == Logger.GetType()) ? ((Logger)Logger).DoEngineLogging : false;
			}
			set
			{
				if (typeof(Logger) == Logger.GetType())
					((Logger)Logger).DoEngineLogging = value;
			}
		}
		public ILogger Logger;

		private ApplicationConfig config;

		public VesselEngine(ApplicationConfig config)
		{
			this.config = config;
		}

		/// <summary>
		/// Initialises vessel and starts the game loop
		/// </summary>
		public void Run(IntPtr handle = default(IntPtr))
		{
			Setup(handle);
		}

		/// <summary>
		/// Called before running any frames, and after the graphics API was setup
		/// Use this method to load data and initialise your game
		/// </summary>
		public virtual void Initialise()
		{

		}
		
		/// <summary>
		/// Called every frame. Used to process the game logic before drawing
		/// </summary>
		public virtual void Update()
		{

		}

		/// <summary>
		/// Called every frame. Used to push draw calls to the screen
		/// </summary>
		public virtual void Draw()
		{
			//  make sure view 0 is cleared if no other draw calls are submitted
			Bgfx.Touch(0);

			// write some debug text
			Bgfx.DebugTextClear();
			Bgfx.DebugTextImage(
				Math.Max(Window.Width / 2 / 8, 20) - 20,
				Math.Max(Window.Height / 2 / 16, 6) - 6,
				40, 12,
				Logo.Bytes,
				160
			);

			Bgfx.DebugTextWrite(0, 1, DebugColor.White, DebugColor.Blue, $"SharpBgfx/Samples/00-HelloWorld (Using {Bgfx.GetBackendName(Bgfx.GetCurrentBackend())})");
			Bgfx.DebugTextWrite(0, 2, DebugColor.White, DebugColor.Cyan, "Description: Initialization and debug text.");

		}

		#region Internal Setup
		/// <summary>
		/// Setups the engine and initialises the window 
		/// </summary>
		internal void Setup(IntPtr handle)
		{
			VesselWindow window;
			if (handle == IntPtr.Zero)
				window = new VesselWindow(GraphicsDevice, config.Name, config.Width, config.Height);
			else
				window = new VesselWindow(GraphicsDevice, handle);
			window.Run(MainLoop);
		}

		internal void MainLoop(VesselWindow window)
		{
			Window = window;

			GraphicsDevice.Initialise(config);
			Window.Invalidate();
			//GraphicsDevice.Reset(window.Width, window.Height, config.GetResetFlags());


			//Bgfx.Init();
			//Window.Invalidate();
			//Bgfx.Reset(window.Width, window.Height, ResetFlags.Vsync);

			// enable debug text
			//Bgfx.SetDebugFeatures(DebugFeatures.DisplayText);

			// set view 0 clear state
			//Bgfx.SetViewClear(0, ClearTargets.Color | ClearTargets.Depth, 0x303030ff);

			Initialise();

			Console.WriteLine("Backend: " + Bgfx.GetBackendName(Bgfx.GetCurrentBackend()));

			while (Window.ProcessEvents(ResetFlags.Vsync))
			{
				Update();

				// set view 0 viewport
				Bgfx.SetViewRect(0, 0, 0, Window.Width, Window.Height);

				Draw();

				// advance to the next frame. Rendering thread will be kicked to
				// process submitted rendering primitives.
				Bgfx.Frame();
			}

			// clean up
			Bgfx.Shutdown();
		}

		#endregion

	}

